//creating an empty array
let myArr=[];

//adding an item to the end of array
myArr.push('five');

//logging new array to the console
console.log(myArr);

//adding a value to beginning of array
myArr.unshift(1);

//adding one more value to an end
myArr.push(30);

myArr.shift(); //removing the first item from an array
myArr.pop();   //removing the last item from an array

//logging an array to console
console.log(myArr)